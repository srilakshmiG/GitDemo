package com.date;

import java.util.Date;

public class Patient {
	
	private String name;
	private Date DOB;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getDOB() {
		return DOB;
	}
	public void setDOB(Date dOB) {
		DOB = dOB;
	}
	
	

}
